# operadores ariméticos
# +- soma
# -- subtração 
# /- divisão
# *- multiplicação 
# %-
# ** - elevado

numero1 = float( input("digite o primerio número: ") )
numero2 = float( input("digite o segundo número: ") )
print(numero1 + numero2)
print( numero1 - numero2)
print(numero1 / numero2)
print(numero1 % numero2)
print(numero1 ** numero2)


